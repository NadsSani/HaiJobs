    public static boolean isValidPostalCode(String code) {
        // must have 6 digits
        if(code.length() != 6)
            return false;

        // make if uppercase for not having to chack for A to Z AND a to z
        code = code.toUpperCase();
        // translate into digit
        char[] digit = code.toCharArray();

        for(int i = 0; i < 4; ++i) {
            if(digit[i] < '0' || digit[i] > '9')
                return false;
        }
        for(int i = 4; i < 6; ++i) {
            if(digit[i] < 'A' || digit[i] > 'Z')
                return false;
        }
        // sounds OK to me
        return true;
    }
    public static boolean checkConnectivity(Context context)
    {
        boolean status=false;
        ConnectivityManager cm = (ConnectivityManager) context
                .getSystemService(Context.CONNECTIVITY_SERVICE);
        NetworkInfo activeNetwork = cm.getActiveNetworkInfo();
        if (null != activeNetwork) {
            if(activeNetwork.getType() == ConnectivityManager.TYPE_WIFI)
                status=true;
            if(activeNetwork.getType() == ConnectivityManager.TYPE_MOBILE)
                status=true;
        }
        else
            status=false;
        return status;
    }

    public static Bitmap rotateImage(Bitmap img, int degree) {
        Matrix matrix = new Matrix();
        matrix.postRotate(degree);
        Bitmap rotatedImg = Bitmap.createBitmap(img, 0, 0, img.getWidth(), img.getHeight(), matrix, true);
        img.recycle();
        return rotatedImg;
    }

    public Bitmap getResizedBitmap(Bitmap image, int maxSize) {
        int width = image.getWidth();
        int height = image.getHeight();

        float bitmapRatio = (float) width / (float) height;
        if (bitmapRatio > 0) {
            width = maxSize;
            height = (int) (width / bitmapRatio);
        } else {
            height = maxSize;
            width = (int) (height * bitmapRatio);
        }
        return Bitmap.createScaledBitmap(image, width, height, true);
    }

    public static Bitmap rotateImageIfRequired(Bitmap img, Uri selectedImage) throws Exception {

        Log.e("HaiJobs","rotateImageIfRequired"+selectedImage.getPath());
        ExifInterface ei = new ExifInterface(selectedImage.getPath());
        int orientation = ei.getAttributeInt(ExifInterface.TAG_ORIENTATION, ExifInterface.ORIENTATION_NORMAL);

        switch (orientation) {
            case ExifInterface.ORIENTATION_ROTATE_90:
                return Utils.rotateImage(img, 90);
            case ExifInterface.ORIENTATION_ROTATE_180:
                return Utils.rotateImage(img, 180);
            case ExifInterface.ORIENTATION_ROTATE_270:
                return Utils.rotateImage(img, 270);
            default:
                return img;
        }
    }



